x = float(input("Введіть координату x: "))
y = float(input("Введіть координату y: "))

if x > 0 and y > 0:
    print("Точка знаходиться в першій координатній чверті")
elif x < 0 and y > 0:
    print("Точка знаходиться в другій координатній чверті")
elif x > 0 and y < 0:
    print("Точка знаходиться в четвертій координатній чверті")
elif x < 0 and y < 0:
    print("Точка знаходиться в третій координатній чверті")
else:
    print("Точка знаходиться на координатних осях або в початку координат")
